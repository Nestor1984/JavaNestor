package _5_bucles;

public class _3_BucleDoWhile {

    public static void main(String[] args) {
        
        // do + tab = do { } while (true);
        
        int i = 0; // iterador
        do { // Hacer
            System.out.println(i); // imprimimos
            i++; // incremento de 1 en 1
        } while (i <= 100); // Mientras
        
        
        
    }
    
}
