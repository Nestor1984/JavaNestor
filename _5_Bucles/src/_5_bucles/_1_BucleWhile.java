package _5_bucles;

public class _1_BucleWhile {

    public static void main(String[] args) {
        
        /*Sintaxis:
        while(condicion){
            //Instrucciones
        }
        */
        
        int edadPablito = 0;
        
        // Bucle while
        while(edadPablito <= 74){ // Mientras esta condicion se cumpla
            System.out.println("Hola, soy Pablito");
            edadPablito++;
        }
        
    }
    
}
